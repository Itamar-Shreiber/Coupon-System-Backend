    @Value("${string.len}")
    private int SIZE;

    public void print(String content) {

        int len = content.length();

        if (len % 2 == 1) {
            int side = ((SIZE - len) / 2) + 1;
            System.out.println();
            System.out.print("_".repeat(side));
            System.out.print(content);
            System.out.println("_".repeat(side));
            System.out.println();
        } else {
            System.out.println();
            int side = (SIZE - len) / 2;
            System.out.print("_".repeat(side));
            System.out.print(content);
            System.out.println("_".repeat(side));
            System.out.println();
        }
    }

